function changeLanguage(lang) {
    console.log("Switching to language: " + lang); // Для проверки в консоли
    localStorage.setItem('site_lang', lang);

    // Обработка алиасов (kk -> kz)
    const activeLang = lang === 'kk' ? 'kz' : lang;
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[activeLang] && translations[activeLang][key]) {
            el.innerHTML = translations[activeLang][key];
        }
    });

    // Плейсхолдеры
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[activeLang] && translations[activeLang][key]) {
            el.placeholder = translations[activeLang][key];
        }
    });

    // Визуальное переключение кнопок
    document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
    document.querySelectorAll(`.lang-btn[data-lang="${lang}"]`).forEach(btn => btn.classList.add('active'));
}

document.addEventListener('DOMContentLoaded', () => changeLanguage(localStorage.getItem('site_lang') || 'ru'));
